class Box
{
    double width;
    double height;
    double depth;
    Box( double w, double h, double d)
    {
        System.out.println("Constructing Box");
        width = w;
        height = h;
        depth = d;
    }

    double volume()
    {
        return width*height*depth;

    }
}
